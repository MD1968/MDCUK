    <h1>Reload</h1> </a>
    <p>In order to create this New Zimbabwe we re-imagine, the transition needs to be controlled managed and directed. In managing our transition RELOAD thus presents our roadmap, and signpost for a future Zimbabwe. The road map entails five key signposts which are the following:</p>

    <ul> 
      <li>Pressure (Political and Diplomatic): Advocacy and mobilization of all progressive and democratic forces to build national consensus on the resolution of the national crisis.</li>
      <li>National Dialogue: credible, bankable, legitimate process of dialogue fully guaranteed by the international community with specific deliverables, benchmarks and timelines through a mutually agreed and acceptable facilitator.</li>
      <li>National Transitional Mechanism: Agreement on an implementing a framework on the agreed positions. This should be done through a national transitional mechanism whose terms in detail are to be agreed.</li>
      <li>Comprehensive Reform Agenda: The implementation and rolling out of a comprehensive agenda, on the Five Point Plan detailed below.</li>
      <li>Free and Fair Elections: Under International Supervision: Electoral reforms are so key to break the vicious cycle and troop of hugely contested elections we must hold free, fair, legitimate, credible and sound elections under international supervision.</li>
    </ul>

